using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MathClassApp
{
    public class Math
    {
        public static int Multiply3(int inputNum)
        {
            int times3 = inputNum * 3;
            return times3;
        }

        public static int Add3(int input1)
        {
            int plus3 = input1 + 3;
            return plus3;
        }

        public static int Divide3(int input2)
        {
            int div3 = input2 / 3;
            return div3;
        }
    }
}
